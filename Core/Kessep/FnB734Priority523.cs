      local.DashboardAuditData.WorkerId = "";

      if (AsChar(entities.Case1.Status) == 'O')
      {
        if (ReadInfrastructure())
        {
          // THIS IS OK TO PROCESS THIS ONE
          if (ReadServiceProviderCaseAssignment2())
          {
